				<p><a href="http://finance.boston.com/boston?Page=QUOTE&amp;Ticker=TER" target="_new">Teradyne Inc.</a>
					of North Reading is cutting 532 of its 3,800 employees worldwide, and imposing a 10 percent pay cut
					on the rest. Teradyne employs 1,055 people in Massachusetts, but the company did not say how many
					local workers will be cut.</p>
				<p>Teradyne, the world's leading maker of equipment for testing microchips, doesn't sell its complex,
					costly products in stores. Instead, they're purchased by major electronics firms around the globe.
					But the decline in consumer electronics sales that has forced retailers such as Circuit City and
					Tweeter out of business has also led to production cuts at chip makers like <a
						href="http://finance.boston.com/boston?Page=QUOTE&amp;Ticker=INTC" target="_new">Intel Corp.</a>
					and <a href="http://finance.boston.com/boston?Page=QUOTE&amp;Ticker=AMD" target="_new">Advanced
						Micro Devices Inc.</a> That means a lot fewer orders for the kind of chip manufacturing gear
					sold by Teradyne.</p>
				<p>"These are very tough times and these are significant cuts that we're making," said Teradyne chief
					executive Michael Bradley. The company's chief financial officer, Gregory Beecher, warned that
					"there is no short-term recovery in sight."</p>
				<p>The layoffs at Teradyne follow a fourth-quarter loss and slower sales. For the three months ended
					Dec. 31, Teradyne lost $55.3 million, or 33 cents a share, compared with a profit of $16.7 million,
					or 10 cents a share, in the same period last year. Revenue declined to $194.8 million from $260.4
					million. Teradyne also warned that it expected to lose between 31 and 38 cents a share in the first
					quarter.</p>
				<p>Another local manufacturer of chip making gear revealed dismal results yesterday. <a
						href="http://finance.boston.com/boston?Page=QUOTE&amp;Ticker=VSEA" target="_new">Varian
						Semiconductor Equipment Associates Inc.</a> of Gloucester reported that revenue for its fiscal
					first quarter ended Jan. 2 was down 58 percent from a year ago. The company went from a $43.7
					million first-quarter profit last year to a $13.6 million loss this year.</p>
				<p>"It's the collapse in consumer demand," said Teradyne spokesman Andy Blanchard. "The consumer, who
					drives about 60 to 70 percent of semiconductor demand, stopped buying in the third quarter."</p>
				<p>Chip makers have responded with large layoffs. Intel, the world's leading semiconductor company, is
					laying off up to 6,000 workers. Rival Advanced Micro Devices will cut 1,100 jobs, and <a
						href="http://finance.boston.com/boston?Page=QUOTE&amp;Ticker=TXN" target="_new">Texas
						Instruments Inc.</a> plans 1,800 layoffs. At the same time, chip makers are reducing output and
					canceling plans for new production lines. That means fewer orders for Teradyne and other companies
					that produce chip making gear.</p>
				<p>Len Jelinek, director and chief analyst for semiconductor manufacturing at research firm iSuppli
					Corp., said semiconductor equipment makers such as Teradyne won't recover any time soon. "All these
					guys are going to suffer significantly in 2009," Jelinek said. "We might see additional factory
					closures or furloughs or even layoffs."</p>
				<p>In a recent report, iSuppli predicted that semiconductor equipment purchases for 2008 would amount to
					$42.7 billion, down 21.1 percent from $54 billion in 2007. Jelinek warned that sales this year could
					decline another 40 percent from last year's figure. Sales may pick up in 2010, because existing
					equipment will become obsolete and must be replaced, but Jelinek predicted that any improvement will
					be slight.</p>
				<p>John Greenagel, spokesman for the Semiconductor Industry Association in San Jose, Calif., said the
					only hope for a turnaround lies in a renewal of consumer demand for computers, TVs, digital cameras,
					and other chip-based products. "Consumers have become the main driver of semiconductor consumption,"
					Greenagel said. "Restoration in consumer confidence is critical to the turnaround in demand."</p>
